
class ColorMap():
    def __init__(self):
        # self.colors = [(0xFF, 0x00, 0x00), (0x00, 0x00, 0xFF), (0x00, 0xFF, 0x00),
        #                (0x00, 0xFF, 0xFF), (0xFF, 0xFF, 0x00), (0xFF, 0x00, 0xFF),
        #                (0xFF, 0xFF, 0xFF), (0xAA, 0x33, 0xAA), (0x68, 0x00, 0x0D),
        #                (0x88, 0x88, 0xFF), (0xFF, 0xAA, 0xAA), (0xAA, 0xFF, 0xFF),
        #                (0xAA, 0xAA, 0x55), (0x55, 0xFF, 0x55), (0x00, 0xA5, 0xFF)]

        self.colors = [(0xFF, 0x00, 0x00), (0x00, 0x00, 0xFF), (0x00, 0xFF, 0x00),
                       (0x00, 0xFF, 0xFF), (0xFF, 0xFF, 0x00), (0xFF, 0x00, 0xFF),
                       (0xFF, 0xAA, 0xAA), (0xAA, 0x33, 0xAA), (0x68, 0x00, 0x0D),
                       (0xAA, 0xAA, 0x55), (0x55, 0xFF, 0x55), (0x00, 0xA5, 0xFF),
                       (0x88, 0x88, 0xFF), (0xAA, 0xFF, 0xFF), (0xFF, 0xFF, 0xFF)
                       ]

        self.num_color = len(self.colors)

    def get(self, idx):
        return self.colors[idx % self.num_color]

    def get_w(self, idx, w):
        color = self.colors[idx % self.num_color]
        r = int(color[0]*w)
        g = int(color[1]*w)
        b = int(color[2]*w)
        return (r, g, b)

def str_time():
    import datetime
    now = datetime.datetime.now()
    time_stamp = '_%d_%d_%d_%d_%d_%d' % ( now.year, now.month, now.day, now.hour, now.minute, now.second)
    return time_stamp